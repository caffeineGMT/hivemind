/**
 * Generate SEO-optimized blog post structure
 */
export function generateBlogPost(topic) {
  const { title, keywords, outline, cta } = topic;

  return {
    meta: {
      title: `${title} | Hivemind Engine Blog`,
      description: `${outline[0]} - Learn ${keywords.join(', ')} with Hivemind Engine.`,
      keywords: keywords.join(', '),
      slug: title
        .toLowerCase()
        .replace(/[^a-z0-9]+/g, '-')
        .replace(/^-+|-+$/g, ''),
    },
    structure: {
      headline: title,
      introduction: `Hook: Why this matters\nPromise: What you'll learn\nPreview: ${outline.join(' → ')}`,
      sections: outline.map((section, i) => ({
        heading: section,
        wordCount: 300,
        includeExample: i % 2 === 0,
        includeImage: i === 0 || i === outline.length - 1,
      })),
      conclusion: {
        recap: 'Summarize key points',
        nextSteps: cta,
        cta: {
          text: cta,
          link: 'https://hivemind-engine.vercel.app/app',
        },
      },
    },
  };
}

/**
 * Generate social media content calendar
 */
export function generateContentCalendar(days = 30) {
  const calendar = [];
  const platforms = ['twitter', 'linkedin', 'reddit'];
  const frequency = {
    twitter: 3, // posts per day
    linkedin: 1, // posts per day
    reddit: 0.2, // posts per day (1 per week)
  };

  for (let day = 0; day < days; day++) {
    const date = new Date();
    date.setDate(date.getDate() + day);

    platforms.forEach((platform) => {
      const postsToday = Math.floor(frequency[platform]);

      for (let i = 0; i < postsToday; i++) {
        calendar.push({
          date: date.toISOString().split('T')[0],
          platform,
          time: platform === 'twitter' ? ['9am', '1pm', '6pm'][i] : '10am',
          content: selectContentForPlatform(platform),
          status: 'scheduled',
        });
      }
    });
  }

  return calendar;
}

function selectContentForPlatform(platform) {
  const content = contentIdeas.socialMediaPosts[platform];
  return content[Math.floor(Math.random() * content.length)];
}

/**
 * Generate content analytics
 */
export function trackContentPerformance(contentId, metrics) {
  return {
    contentId,
    views: metrics.views || 0,
    clicks: metrics.clicks || 0,
    conversions: metrics.conversions || 0,
    ctr: metrics.clicks / metrics.views || 0,
    conversionRate: metrics.conversions / metrics.clicks || 0,
    estimatedValue: metrics.conversions * 199, // Assume Pro plan
  };
}

